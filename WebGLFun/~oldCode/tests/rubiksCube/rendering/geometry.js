function setGeometry(gl, name)
{
  var modelGeometry;

  if(name == "quad")
  {
    modelGeometry = quad;
  }
  else if(name == "cubieVerts")
  {
    modelGeometry = cubieVerts;
  }
  else if(name == "screenQuad")
  {
    modelGeometry = screenQuad;
  }


	gl.bufferData(gl.ARRAY_BUFFER, new Float32Array(modelGeometry), gl.STATIC_DRAW);  // send the attributes data down to GPU
}
